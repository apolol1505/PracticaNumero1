    private void cargarTabla() {
        modelo.setDatos(sd.listar());
        tblTabla.setModel(modelo);
        tblTabla.updateUI();
    }

    private void limpiar() {
        this.sd.setSucursal(null);
        txtNombre.setText("");
        cargarTabla();
    }

    public void irAVentas() {
        try {
            int fila = tblTabla.getSelectedRow();

            if (fila >= 0) {
                if (modelo.getDatos().get(fila) != null) {
                    this.sd.setSucursal(modelo.getDatos().get(fila));
                    SucursalControl sc = new SucursalControl();
                    sc.setSucursal(sd.getSucursal());
                    new FrmVentas(null, true, sc).setVisible(true);
                    cargarTabla();
                    tblTabla.clearSelection();
                    this.sd.modificar(this.sd.getSucursal(), fila);
                    limpiar();
                } else {
                    JOptionPane.showMessageDialog(null, "seleccione una sucursal", "ERROR", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(null, "sucursal vacia", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
        }
    }

    private void guardarSucursal() {

        if (!this.txtNombre.getText().trim().isEmpty()) {
            this.sd.getSucursal().setNombre(this.txtNombre.getText());
            try {
                this.sd.guardar();// Llamada al m√©todo registrar()
                limpiar();
                JOptionPane.showMessageDialog(null, "Guardado correctamente", "GUARDADO", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
            }
            limpiar();
        } else {
            JOptionPane.showMessageDialog(null, "Los campos no pueden quedar en blanco", "ERROR", JOptionPane.ERROR_MESSAGE);

        }
        

    }

    private void irAPeticiones() {
        new FrmPeticiones(null, true).setVisible(true); // Llamar a FrmPeticiones

    }

